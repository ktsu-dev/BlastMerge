	/// </summary>
	FeatureGuide,

	/// <summary>
	/// Show keyboard shortcuts and navigation help
	/// </summary>
	KeyboardShortcuts,

	/// <summary>
	/// Return to the main menu
	/// </summary>
	BackToMainMenu
}
